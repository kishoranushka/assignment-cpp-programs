//wap to check whether a given number is even or odd

#include<iostream> 
 using namespace std; 
 int main()
 {
    int n;
    cout<<"enter any number: ";
    cin>>n;

    if(n%2==0)
    {
        cout<<"the number is even.";
    }
   
    else
    {
        cout<<"the number is odd.";
    }

     


return 0;
}